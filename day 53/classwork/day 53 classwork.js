// ჩვენი ორი მოცემული ცვლადი რიცხვი
let num1 = 10;
let num2 = 5;

// დავამატოთ რიცხვები ერთმანეთთან
let sum = num1 + num2; // რიცხვების დამატება
console.log("Sum:", sum); // output არის 15

// გამოკლება რიცხვების
let difference = num1 - num2; // გამოკლება
console.log("Difference:", difference); // output არის 5

// რიცხვების გამრავლება
let product = num1 * num2; // გამრავლება
console.log("Product:", product); // output არის 50

// რიცხვების გაყოფა
let quotient = num1 / num2; // გაყოფა
console.log("Quotient:", quotient); // output არის 2














