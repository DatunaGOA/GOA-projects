// ვქმნით ცვლადებს მასივის ზომისთვის
const rows = 5; // რიგების რაოდენობა (5)
const cols = 6; // სვეტების რაოდენობა (6)

// ვქმნით ცარიელ მასივს, რომელშიც შევინახავთ მონაცემებს
let matrix = [];

// ციკლი მონაცემების შესაყვანად
for (let i = 0; i < rows; i++) {
    // ვთხოვთ მომხმარებელს შეიყვანოს 6 სიმბოლო, გამოყოფილი სივრცეებით
    let rowInput = prompt(`შეიყვანეთ ${cols} სიმბოლო გამოყოფილი სივრცეებით (რიგი ${i + 1}):`);
    
    // ვყოფთ შეყვანილ სტრიქონს მასივად და ვიღებთ მხოლოდ 6 ელემენტს
    let rowArray = rowInput.split(" ").slice(0, cols);
    
    // მიღებულ რიგს ვამატებთ 2D მასივში
    matrix.push(rowArray);
}

// პირველი და ბოლო ელემენტების გაცვლა
let firstElement = matrix[0][0]; // ვინახავთ პირველ ელემენტს (ზედა მარცხენა)
let lastElement = matrix[rows - 1][cols - 1]; // ვინახავთ ბოლო ელემენტს (ქვედა მარჯვენა)

// ვუცვლით ადგილებს პირველ და ბოლო ელემენტებს
matrix[0][0] = lastElement;
matrix[rows - 1][cols - 1] = firstElement;

// საბოლოო შედეგის გამოტანა კონსოლში
console.log("\nმასივი პირველ და ბოლო ელემენტების შეცვლის შემდეგ:");
matrix.forEach(row => console.log(row.join(" "))); // თითოეული რიგის დაბეჭდვა
// კომენტარებს იმიტომ ვამატებ რომ უკეთესად გავიგო, ეს შეჯიბრი როა თან ვემზადები და ვსწავლობ